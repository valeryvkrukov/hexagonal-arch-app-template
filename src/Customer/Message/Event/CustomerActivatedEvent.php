<?php

namespace App\Customer\Message\Event;

use App\Message\AsyncMessageInterface;

final class CustomerActivatedEvent implements AsyncMessageInterface
{
    /**
     * @var int
     */
    private int $id;

    /**
     * @param int $id
     */
    public function __construct(int $id)
    {
        $this->id = $id;
    }

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }
}